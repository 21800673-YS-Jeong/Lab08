/**
 * This class capitalize every characters in stringValue.
 * @author YunSeok Jeong
 */
public class Util {
    /**
     * This method capitalize every characters in stringValue.
     * @param stringValue
     * @return stringValue.toUpperCase()
     */
    static String getUppercaseString(String stringValue) {
        return stringValue.toUpperCase();
    }
    
}
